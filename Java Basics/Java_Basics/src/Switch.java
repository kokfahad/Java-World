public class Switch {
    public static void main(String[] args) {
        int var = 15;

        switch (var){
            case 5:
                System.out.println("Value is 5");
                break;
            case 10:
                System.out.println("Value is 10");
                break;
            default:
                System.out.println("No values matched");

        }
    }
}
